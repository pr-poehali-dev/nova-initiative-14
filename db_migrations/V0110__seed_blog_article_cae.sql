## Что изменилось

Через пару месяцев коллега по кафедре показал мне облачный CAE — расчётный инструмент, который работает прямо в браузере. Ничего не нужно устанавливать: открыл вкладку, нарисовал схему, нажал «рассчитать».

Звучит просто. Но именно эта простота меня и зацепила. Вот что он делает:

- **Рисуешь раму или балку мышкой** — узлы и стержни прямо на сетке, с привязкой. Опоры, шарниры, распределённые нагрузки — всё на месте.
- **Считает по методу конечных элементов** — на сервере, честно, с учётом сдвиговых деформаций. Не «примерно», а как положено.
- **Сразу строит эпюры** N, Q, M и эквивалентные напряжения по Мизесу. Те самые, над которыми я страдал ночами.
- **Проверяет прочность и прогиб** автоматически — сравнивает с допускаемыми по нормам и говорит, проходит конструкция или нет.
- **Внутри — каталог профилей по ГОСТ**: двутавры, швеллеры, уголки, трубы. Выбрал сечение — характеристики подставились сами.

А та самая просьба научрука «а если нагрузка вырастет»? Теперь это десять секунд: поменял число, нажал кнопку, получил новую эпюру. Думаешь над результатом, а не над арифметикой.

## Самое приятное — отчёт

Отдельно скажу про то, что спасло меня на защите. Инструмент формирует **PDF-отчёт по ЕСКД** — с рамкой, формулами, схемой и эпюрами. Его можно прямо вкладывать в приложение к диплому. Комиссия видит аккуратный документ, а не сфотографированный лист в клеточку.

Для тех, кто оформлял ВКР, объяснять не надо, сколько это экономит времени и нервов.

## Честно про ограничения

Не буду приукрашивать. Сейчас инструмент идеально считает **плоские (2D) схемы** — рамы, балки, фермы. Это закрывает большинство учебных и многих рабочих задач.

**3D уже на подходе** — пространственные конструкции с кручением и двухосным изгибом. Расчётное ядро для них готово и проверено на эталонных задачах, сейчас допиливают удобный интерфейс. Так что скоро можно будет крутить модель в пространстве так же легко, как сейчас рисуешь плоскую схему.

