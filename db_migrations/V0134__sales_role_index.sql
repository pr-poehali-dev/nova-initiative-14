-- Роль «Менеджер по продажам» (sales) хранится в существующей таблице
-- sso_user_roles (role VARCHAR). Отдельная колонка не нужна — роль выдаётся
-- строкой 'sales'. Эта миграция фиксирует факт ввода роли и гарантирует индекс
-- для быстрой выборки пользователей по роли.
CREATE INDEX IF NOT EXISTS idx_sso_user_roles_role ON sso_user_roles(role);

-- Документируем допустимое значение роли продавца (информативно).
COMMENT ON COLUMN sso_user_roles.role IS 'Роль пользователя: sales (менеджер по продажам) и др.';
