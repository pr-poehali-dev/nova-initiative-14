/** Подсчёт задач по статусам — для шапки страницы. */
export function roadmapStats() {
  const all = PLM_ROADMAP.flatMap((p) => p.tasks);
  return {
    total: all.length,
    done: all.filter((t) => t.status === "done").length,
    progress: all.filter((t) => t.status === "progress").length,
  };
}
