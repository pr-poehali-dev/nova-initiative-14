/**
 * Дорожная карта развития сервиса в сторону PLM (тикет №43).
 *
 * Это продуктовый план: разбивает «великую затею» из тикета на конкретные
 * этапы и задачи, чтобы было видно, что уже есть, что в работе и что впереди.
 * Данные статические (контент авторский, меняется редко) — отдельная страница
 * /cae/roadmap читает этот файл. Публичная часть полезна и как сигнал на
 * лендинге: «сервис активно развивается в направлении PLM».
 *
 * Статусы задач:
 *  - done     — уже реализовано (есть в продукте);
 *  - progress — в активной работе;
 *  - next     — ближайшее, спланировано;
 *  - later    — позже / требует крупной подготовки;
 *  - research — нужно исследование/решение до старта.
 */

export type RoadmapStatus = "done" | "progress" | "next" | "later" | "research";

export interface RoadmapTask {
  title: string;
  status: RoadmapStatus;
  /** Короткое пояснение: что это и зачем. */
  note?: string;
}

export interface RoadmapPhase {
  /** Номер/код этапа для якоря. */
  key: string;
  /** Заголовок этапа. */
  title: string;
  /** Иконка lucide. */
  icon: string;
  /** Грубый горизонт. */
  horizon: string;
  /** Краткое описание этапа. */
  summary: string;
  tasks: RoadmapTask[];
}

