/**
 * Скрипты продаж для менеджеров (сервис «Скрипты продаж» в личном кабинете).
 * Доступ: роль sales, а также администратор и владелец.
 *
 * Структура: 4 темы — презентация компании и три направления продаж
 * (наставничество, CAE для физлиц/организаций, CAE-виджет). Каждая тема
 * состоит из блоков: открытие, ценность, отработка возражений, закрытие.
 */
import type { SsoUser } from "@/lib/auth";

/** Доступ к сервису скриптов: продавец, администратор или владелец. */
export function canSeeSalesScripts(user: SsoUser | null | undefined): boolean {
  if (!user) return false;
  return (
    !!user.is_admin ||
    !!user.is_owner ||
    (Array.isArray(user.roles) && user.roles.includes("sales"))
  );
}

export interface ScriptBlock {
  /** Заголовок блока внутри скрипта (этап разговора). */
  heading: string;
  /** Иконка lucide для блока. */
  icon: string;
  /** Реплики/тезисы менеджера. */
  lines: string[];
}

export interface SalesScript {
  slug: string;
  /** Название направления. */
  title: string;
  /** Короткий подзаголовок — для кого и о чём. */
  tagline: string;
  icon: string;
  /** Целевая аудитория этого скрипта. */
  audience: string;
  /** Ключевая выгода в одной фразе (elevator pitch). */
  pitch: string;
  blocks: ScriptBlock[];
  /** Частые возражения и готовые ответы. */
  objections: { q: string; a: string }[];
  /** Чек-лист перед закрытием сделки. */
  checklist: string[];
}

