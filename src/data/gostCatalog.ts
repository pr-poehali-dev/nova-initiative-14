/**
 * Каталог ГОСТов по разделам инженерии.
 *
 * Реализован по запросу из тикета поддержки: помогает находить нормативные
 * документы даже когда пользователь не знает номера ГОСТа, но понимает суть
 * запроса. Для этого у каждого документа есть поле `tags` — синонимы и
 * бытовые формулировки темы (например «лестницы», «ограждения», «перила»),
 * по которым работает смысловой поиск на странице.
 */

export interface GostItem {
  /** Обозначение, напр. "ГОСТ 8239-89" */
  code: string;
  /** Название документа */
  title: string;
  /** Синонимы/бытовые формулировки темы для поиска «по смыслу» */
  tags: string[];
}

export interface GostSection {
  /** Короткий маркер для угловой зоны рамки (1-3 символа) */
  marker: string;
  /** Иконка lucide */
  icon: string;
  /** Название раздела */
  title: string;
  /** Описание раздела */
  hint: string;
  items: GostItem[];
}

