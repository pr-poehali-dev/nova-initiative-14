/**
 * Каталог ГОСТов по разделам инженерии — трёхуровневая структура.
 *
 * Отрасль (GostDomain) → Тематическая подгруппа (GostGroup) → Стандарт (GostItem).
 *
 * Идея навигации (по запросу из тикета #68): развернул отрасль «Машиностроение»
 * — увидел подгруппы «Балки и профили», «Резьбы и крепёж», «Допуски и посадки»,
 * «Метрология», «Стандартизация и сертификация» и т.д.; раскрыл подгруппу —
 * увидел компактный список ГОСТов. Всё находится листанием, без строки поиска.
 * Теги (`tags`) оставлены для опционального смыслового поиска.
 */

export interface GostItem {
  /** Обозначение, напр. "ГОСТ 8239-89" */
  code: string;
  /** Краткое название документа */
  title: string;
  /** Синонимы/бытовые формулировки темы для поиска «по смыслу» */
  tags: string[];
}

export interface GostGroup {
  /** id подгруппы (уникален в рамках отрасли) */
  id: string;
  /** Название тематической подгруппы */
  title: string;
  items: GostItem[];
}

export interface GostDomain {
  /** id отрасли */
  id: string;
  /** Короткий маркер для угловой зоны рамки */
  marker: string;
  /** Иконка lucide */
  icon: string;
  /** Название отрасли */
  title: string;
  /** Короткое описание отрасли */
  hint: string;
  groups: GostGroup[];
}

